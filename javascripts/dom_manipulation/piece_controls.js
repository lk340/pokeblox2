export const movePiece = () => {
  document.addEventListener("keydown", event => {
    switch(event.which) {
      case 87: // w
      case 65: // a
      case 83: // s
      case 68: // d
      case 38: // up
      case 37: // left
      case 40: // down
      case 39: // right
      case 32: // space-bar
      case 16: // shift
    }
  });
};